        public override string[] GetRolesForUser(string username)
        {
            using (EMSContext context = new EMSContext())
            {
                var userRoles = (from user in context.UserProfile
                                 join role in context.Role
                                 on user.FKRoleId equals role.PKRoleId
                                 where user.EmailId == username
                                 select role.RoleName).ToArray();
                return userRoles;
            }
        }

